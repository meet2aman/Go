package main

import "fmt"

func main() {

	/// String
	var name string = "Loki"
	fmt.Println("This is the String", name)

	/// Boolean
	var isTrue bool = true
	fmt.Println("This is the Boolean", isTrue)

	/// General Integer
	var z int = 25
	fmt.Println("This is the General Number", z)

	// Unsigned integer
	var unSignedInteger uint16 = 30
	fmt.Println("This is the Unsigned Number", unSignedInteger)

	/// Signed Integer
	var signedInteger int16 = -15
	fmt.Println("This is the Signed Number", signedInteger)

	/// float
	var decimalNumber float32 = 24.349374873
	fmt.Println("This is the Float Number", decimalNumber)

}
